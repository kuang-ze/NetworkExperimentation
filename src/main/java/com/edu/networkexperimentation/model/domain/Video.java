    private String title;

    /**
     * 实际位置
     */
    private String file;

    /**
     * 上传者
     */
    private Long uploadUser;

    /**
     * 所属小节
     */
    private Long sectionID;

    /**
     * 创建时间
     */
    private Date createTime;

